// Mortgage payment helper for use inside answer bodies
const mortgagePI = (loan, ratePct, years) => {
  const r = ratePct / 100 / 12;
  const n = years * 12;
  return loan * (r * Math.pow(1 + r, n)) / (Math.pow(1 + r, n) - 1);
};

